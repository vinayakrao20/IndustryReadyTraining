# 6. Write a program to find the area and circumference of a circle.
import math
radius = int(input("Enter Radius  "))
pi = math.pi # in built pi value 
print(f' Radius of circle is {radius} and area is {pi*(radius**2)} and cercumfrence is {2*radius*pi}')
# circle area = pi*r^2 and circumference = 2*pi*r
